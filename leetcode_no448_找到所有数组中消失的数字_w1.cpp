vector<int> findDisappearedNumbers(vector<int>& nums) {
    int n = nums.size();
    for (int& num : nums) {
        int x = (num - 1) % n;
        nums[x] += n;
    }

    vector<int> res;
    for (int i = 0; i < n; i++) {
        if (nums[i] <= n) {
            res.emplace_back(i + 1);
        }
    }
    return res;
}